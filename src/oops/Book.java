package oops;
class Book {
    String title;
    String author;
    int price;
    String description;
    int publish_year;
    String publisher_name;

    public Book(String title, String author, int price, String description, int publish_year, String publisher_name) {

        this.title = title;
        this.author = author;
        this.price = price;
        this.description = description;
        this.publish_year = publish_year;
        this.publisher_name = publisher_name;

    }
}